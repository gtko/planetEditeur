    private void jChoixActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jChoixActionPerformed
            System.out.print(evt.getActionCommand());
           
            if(evt.getActionCommand().equals("CancelSelection"))
            {
                jFrame2.setVisible(false);
            }    
            else
            {
                if(choix == 'O')
                {    
                    jFrame2.setVisible(false);
                    File file = jChoix.getSelectedFile();
                    Edit tab = new Edit();
                    jEtat.setText("Ouverture en cours");
                    tabEdit.add(tab);
                    tab.setTab(jTab.getTabCount());
                    tab.setFichier(file);
                    jTab.addTab(file.getName(),tabEdit.elementAt(jTab.getTabCount()).getjScroll());
                    jTab.setSelectedIndex(jTab.getTabCount() - 1);
                   JTextArea texte = tabEdit.elementAt(jTab.getSelectedIndex()).getjTexte();
                    texte.addFocusListener(new appFocusListener("jTexte"));
                    texte.addKeyListener(new appKeyListener("jTexte" , jList ,texte));
                    jEtat.setText("Fichier ouvert");
                    
                }
                else if (choix == 'S')
                {
                    String name;
                    String rep;
                    jFrame2.setVisible(false);

                    //ON REGARDE LE FICHIER CHOISI POUR L'ENREGISTREMENT
                    File file = jChoix.getSelectedFile(); 
                    //ON RECUPERE LE TAB SELECTIONNER POUR AVOIR L'EDIT ACTUEL
                    Edit sauve = tabEdit.elementAt(jTab.getSelectedIndex());
                    
                    //ON PREPARE LE INFO POUR L'ENREGISTREMENT
                    name = file.getName();
                    rep = file.getPath();
                    sauve.setNom(name);
                    sauve.setRep(rep);
                  
                    System.out.print("\n" + rep );
                    try{
                        //ON SAUVEGARDE
                        jEtat.setText("sauvegarde en cour");
                        sauve.sauvegarde();
                        //ON CHANGE LE FICHIER DE L'EDIT
                        sauve.setFichier(new File(rep));
                        jTab.setTitleAt(jTab.getSelectedIndex(),sauve.getNom());
                        jEtat.setText("Sauvegarde effectuer");
                         
                    } 
                    catch (IOException ex) {
                    Logger.getLogger(gui.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
            
           
    }//GEN-LAST:event_jChoixActionPerformed

